function generateStudents() {
    return arabicNames.map((name, index) => {
        const responseTime = Math.floor(Math.random() * 50) + 10;
        const engagement = Math.floor(Math.random() * 65) + 30;
        const exits = Math.floor(Math.random() * 6);
        
        let status = 'normal';
        let score = 0;
        
        if (responseTime > 40) score += 2;
        else if (responseTime > 25) score += 1;
        
        if (engagement < 50) score += 2;
        else if (engagement < 70) score += 1;
        
        if (exits > 3) score += 2;
        else if (exits >= 2) score += 1;
        
        if (score >= 4) status = 'urgent';
        else if (score >= 2) status = 'warning';
        
        return {
            id: index + 1,
            name: name,
            responseTime: responseTime,
            normalResponseTime: 20,
            engagement: engagement,
            normalEngagement: 85,
            exits: exits,
            status: status
        };
    });
}

